-- Master Tables

DROP TABLE IF EXISTS REF_MEM_TYPE;
CREATE TABLE REF_MEM_TYPE
(
TYPE_ID Int NOT NULL AUTO_INCREMENT,
MEM_TYPE varchar(30),
PRIMARY KEY (TYPE_Id)
);

DROP TABLE IF EXISTS REF_GENDER;
CREATE TABLE REF_GENDER
(
GENDER_ID Int NOT NULL AUTO_INCREMENT,
GENDER varchar(10),
PRIMARY KEY (GENDER_ID)
);

DROP TABLE IF EXISTS REF_REGION;
CREATE TABLE REF_REGION
(
REG_ID INT NOT NULL AUTO_INCREMENT,
PREFIX VARCHAR(4),
COUNTRY VARCHAR (100),
STATE VARCHAR(100),
CITY VARCHAR(100),
ZIP INT,
PRIMARY KEY (REG_ID)
);

DROP TABLE IF EXISTS MEMBER;
CREATE TABLE MEMBER
(
MEM_ID INT NOT NULL AUTO_INCREMENT,
F_NAME Varchar(50),
L_NAME Varchar(50),
GENDER INT, FOREIGN KEY (GENDER) REFERENCES GENDER(GEN_ID),
M_PREFIX varchar(5),
MOBILE int CHECK (LEN(10)),
EMAILID varchar(20),
ADD_1 varchar(50),
ADD_2 Varchar(50),
CITY Varchar(10),
STATE Varchar (20),
ZIP varchar(6),
MEM_TYPE int,
PRIMARY KEY (MEM_ID),
FOREIGN KEY (MEM_TYPE) REFERENCES MEM_TYPE(TYPE_ID)
);

DROP TABLE IF EXISTS MEM_LOGIN;
Create Table MEM_LOGIN
(
MEM_ID int , FOREIGN KEY (MEM_ID) REFERENCES MEMBER(Id),
EmailId varchar(50),
ENCR_PWD varchar(4000)
);



